<p><img src="../../../images/medical_symbol.png" alt="Medical Symbol" style="float:right" height="150">
    Snap for Life is a web application that enables emergency medical responders 
    to collect immediate patient information and images in the field, such as at
    the scene of an accident or in an ambulance. They will be able to select the 
    type of accident and a receive a simple set of customizable questions to 
    answer about the patient’s condition. The set of questions will be 
    configurable ahead of time by hospital administration to match a particular 
    medical situation. The medical responders on site will be able to upload the 
    photos and information of the patent’s energies to be accessed quickly and 
    securely via a web page by the emergency room personnel.</p>

<p>The objective of this application is to reduce hospital mortality rates by 
    improving the speed and quality of communication between medical emergency 
    responders and hospitals in order to enable better preparation and care for 
    the patient. The data will not be stored on the user’s personal mobile device 
    so that HIPAA violations are safely avoided.</p>

